def decode_bits(bits):
    return str(bits).replace('111111', '-').replace('000000', ' ').replace('111', '-').replace('000', ' ').replace('11', '.').replace('00', '').replace('1', '.').replace('0', '')

def decode_morse(morseCode):
    eng_to_morse = {'.-': 'a', '-...': 'b', '-.-.': 'c', '-..': 'd', '.': 'e', '..-.': 'f', '--.': 'g', '....': 'h', '..': 'i', '.---': 'j', '-.-': 'k', '.-..': 'l', '--': 'm', '-.': 'n', '---': 'o', '.--.': 'p', '--.-': 'q', '.-.': 'r', '...': 's', '-': 't', '..-': 'u', '...-': 'v', '.--': 'w', '-..-': 'x', '-.--': 'y', '--..': 'z', '.-.-.-': '.', '..--..': '?', '--..--': ',', '': ' '}
    return "".join([eng_to_morse[i] for i in morseCode.split(" ")]).upper()


